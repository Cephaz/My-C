char		*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	i = 0;
	while (str[i])
	{
		while ((str[i] >= '0' && str[i] <= '9') || (str[i] >= 'A'
					&& str[i] <= 'Z') || (str[i] >= 'a' && str[i] <= 'z'))
		{
			i++;
			if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
