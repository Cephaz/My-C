char		*ft_strupcase(char *str)
{
	char *str2;

	str2 = str;
	while (*str)
	{
		if (*str >= 97 && *str <= 122)
		{
			*str = *str + 'A' - 'a';
		}
		str++;
	}
	return (str2);
}
